import os
import fastf1
os.makedirs("/content/f1_cache", exist_ok=True)

fastf1.Cache.enable_cache("/content/f1_cache")

import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.ensemble import GradientBoostingRegressor
from sklearn.metrics import mean_absolute_error

"""Loading FastF1 race session (2024 Australian GP, Round 3) and getting lap data"""

session_2024 = fastf1.get_session(2024, 3, "R")
session_2024.load()

laps_2024 = session_2024.laps[["Driver", "LapTime"]].copy()
laps_2024.dropna(subset=["LapTime"], inplace=True)
laps_2024["LapTime (s)"] = laps_2024["LapTime"].dt.total_seconds()

"""Manually defining the data for 2025 qualifying times for all drivers"""

qualifying_2025 = pd.DataFrame({
    "Driver": ["Lando Norris", "Oscar Piastri", "Max Verstappen", "George Russell", "Yuki Tsunoda",
               "Alexander Albon", "Charles Leclerc", "Lewis Hamilton", "Pierre Gasly", "Carlos Sainz", "Fernando Alonso", "Lance Stroll"],
    "QualifyingTime (s)": [75.096, 75.180, 75.481, 75.546, 75.670,
                           75.737, 75.755, 75.973, 75.980, 76.062, 76.4, 76.5]
})

"""FastF1 use 3 letter code for all drivers.hence defining it for all drivers in the GP"""

#Step 6: Map driver names to FastF1 codes
driver_mapping = {
    "Lando Norris": "NOR", "Oscar Piastri": "PIA", "Max Verstappen": "VER", "George Russell": "RUS",
    "Yuki Tsunoda": "TSU", "Alexander Albon": "ALB", "Charles Leclerc": "LEC", "Lewis Hamilton": "HAM",
    "Pierre Gasly": "GAS", "Carlos Sainz": "SAI", "Lance Stroll": "STR", "Fernando Alonso": "ALO"
}

qualifying_2025["DriverCode"] = qualifying_2025["Driver"].map(driver_mapping)

#Step 7: Merge Qualifying with 2024 Race Lap Times
merged_data = qualifying_2025.merge(laps_2024, left_on="DriverCode", right_on="Driver")

# Feature and target
X = merged_data[["QualifyingTime (s)"]]
y = merged_data["LapTime (s)"]

if X.shape[0] == 0:
    raise ValueError("Dataset is empty after preprocessing. Check data sources!")

"""Using GradientBoostingRegressor from Scikit-learn to train prediction model:"""

#Step 8: Train Gradient Boosting Regressor
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2, random_state=37)
model = GradientBoostingRegressor(n_estimators=100, learning_rate=0.1, random_state=37)
model.fit(X_train, y_train)

# Step 9: Predict Race Lap Times based on 2025 Qualifying
predicted_lap_times = model.predict(qualifying_2025[["QualifyingTime (s)"]])
qualifying_2025["PredictedRaceTime (s)"] = predicted_lap_times

# Rank by predicted lap times
qualifying_2025 = qualifying_2025.sort_values(by="PredictedRaceTime (s)")

# Final Prediction Output
print("\n Predicted 2025 Chinese GP Winner \n")
print(qualifying_2025[["Driver", "PredictedRaceTime (s)"]])

#Step 10: Evaluate Model
y_pred = model.predict(X_test)
print(f"\nüîç Model Error (MAE): {mean_absolute_error(y_test, y_pred):.2f} seconds")